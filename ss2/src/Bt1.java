public class Bt1 {
    public static void main(String[] args) {
        int a = 10;
        double b = 5.5;
        char c = 'A';
        String str = "Hello, Java!";

        int sum = a + 20;
        double radius = 3.0;
        double area = Math.PI * radius * radius;

        System.out.println("Giá trị của a: " + a);
        System.out.println("Giá trị của b: " + b);
        System.out.println("Giá trị của c: " + c);
        System.out.println("Giá trị của str: " + str);
        System.out.println("Tổng của hai số nguyên: " + sum);
        System.out.println("Diện tích hình tròn có bán kính " + radius + " là: " + area);
    }
}
